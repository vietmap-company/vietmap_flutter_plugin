enum TileMapType {
  /// The default style for VIETMAP maps, suitable for general use.
  vectorDefault,

  /// A lighter version of the default style, optimized for readability in bright conditions.
  vectorLight,

  /// A dark-themed style for better visibility in low-light conditions.
  vectorDark,

  /// Raster tiles for the default style, suitable for traditional web mapping libraries.
  rasterDefault,

  /// Raster tiles for the light style, optimized for readability in bright conditions.
  rasterLight,

  /// Raster tiles for the dark style, suitable for low-light conditions.
  rasterDark,
}
